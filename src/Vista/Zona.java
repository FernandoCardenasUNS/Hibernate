    public void Insertar(){
        try {
            String SQL="INSERT INTO zona(id_zona,descripcion)values(?,?)";
            PreparedStatement pst=con.prepareStatement(SQL);
            pst.setString(1, txtid.getText());
            pst.setString(2, txtdes.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Registro Exitoso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de registro" + e.getMessage());
        }
    }
    public void Actualizar(){
        try {
            String SQL="UPDATE zona SET id_zona=?,descripcion=? WHERE id_zona=?";
            int filaSeleccionada=tablazona.getSelectedRow();
            String dao=(String)tablazona.getValueAt(filaSeleccionada,0);
            PreparedStatement pst=con.prepareStatement(SQL);
            pst.setString(1, txtid.getText());
            pst.setString(2, txtdes.getText());
            pst.setString(3,dao);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Registro Editado Exitoso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de Edicion" + e.getMessage());
        }
    }
    public void Limpiar(){
        txtid.setText("");
        txtdes.setText("");
    }
    public void Mostrar(){
        String[] titulo={"Id","Descripcion"};
        String[] registro=new String[3];
        DefaultTableModel modelo= new DefaultTableModel(null,titulo);
        String SQL="SELECT * FROM zona";
        try {
            Statement st= con.createStatement();
            ResultSet rs= st.executeQuery(SQL);
            while (rs.next()) {
                registro[0]=rs.getString("id_zona");
                registro[1]=rs.getString("descripcion");
                modelo.addRow(registro);
            }
            tablazona.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar datos" + e.getMessage());
        }
    }
    public void Filtrar(String valor){
        String[] titulo={"Id","Descripcion"};
        String[] registro=new String[3];
        DefaultTableModel modelo= new DefaultTableModel(null,titulo);
        String SQL="SELECT * FROM zona WHERE descripcion like '%"+valor+"%'";
        try {
            Statement st= con.createStatement();
            ResultSet rs= st.executeQuery(SQL);
            while (rs.next()) {
                registro[0]=rs.getString("id_zona");
                registro[1]=rs.getString("descripcion");
                modelo.addRow(registro);
            }
            tablazona.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar datos" + e.getMessage());
        }
    }
    public void Eliminar(){
        int filaSeleccionada=tablazona.getSelectedRow();
        try {
            String SQL="DELETE FROM zona WHERE id_zona="+tablazona.getValueAt(filaSeleccionada,0);
            Statement st=con.createStatement();
            int n=st.executeUpdate(SQL);
            if (n>=0) {
                JOptionPane.showMessageDialog(null,"Registro eliminado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error al Eliminar Registros " + e.getMessage());
        }
    }

